/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	
  static uint16_t Temp=0;//时钟计时
  Temp++;
  if(Temp == 1000) 
  {
	  Temp=0;
	  Second++;
	  if(Second == 60)
      {
		  Second=0;
		  Minute++;
		  if(Minute == 60)
	      {
			  Minute=0;
		      Hour++;
			  if(Hour == 24)
				  Hour=0;
          }
      }
  }  
  
  H1 = Hour/10;
  H2 = Hour%10;
  
  M1 = Minute/10;
  M2 = Minute%10;
  
  S1 = Second/10;
  S2 = Second%10;
  
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */

  /* USER CODE END SysTick_IRQn 1 */
}

